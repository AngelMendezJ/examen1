	<h2>El sistema metrico decimal</h2>
	<p>El sistema métrico decimal es un sistema de medida que tiene por unidades básicas el metro y el kilogramo, en el cual los múltiplos o submúltiplos de las unidades de una misma naturaleza siguen una escala decimal. Este sistema, ampliado y reformado, ha dado lugar al Sistema Internacional de Unidades</p>
	<h2>Sistema ingles de medidas</h2>
	<p>El sistema anglosajón de unidades es un conjunto de unidades de medida diferentes a las del Sistema métrico decimal, que se utilizan actualmente como medida principal en los Estados Unidos, el Reino Unido (antiguamente) y en algunos territorios históricamente vinculados a estos dos países como es el caso de Puerto Rico, un Estado libre asociado de Estados Unidos.</p>
